public class Film {
    private String name;
    private int ageRating;

    public Film(String filmName, int filmAgeRating) {
        this.name = filmName;
        this.ageRating = filmAgeRating;
    }
    
    public String name(){
        String filmName = this.name;
        
        return filmName;
    }
    
    public int ageRating(){
        int ageRating = this.ageRating;
        
        return ageRating;
    }
    
    
    
}
